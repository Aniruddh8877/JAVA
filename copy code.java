                        //Question: Explain the difference between == and .equals() in Java.


// The == operator is used for reference comparison. It checks if two references point to the same memory location.
// The .equals() method is used for content comparison. It compares the content of two objects.
// Example:

//             String str1 = new String("Hello");
//             String str2 = new String("Hello");
//             System.out.println(str1 == str2);            Output: false (reference comparison)
//             System.out.println(str1.equals(str2));       Output: true (content comparison)

